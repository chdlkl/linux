#!/bin/bash
read -p "please input your source:(0-100):" GRADE
if [ $GRADE -ge 85 ] && [ $GRADE -le 100 ]; then  #注意分号
echo "$GRADE is Excellent!"
elif [ $GRADE -ge 70 ] && [ $GRADE -le 84 ]; then
echo "$GRADE is pass!"
elif [ $GRADE -ge 0 ] && [ $GRADE -le 69 ]; then
echo "GRADE is fail!"
else echo "$GRADE is wrong!!!"
fi
